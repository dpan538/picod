import SwiftUI

struct PetView: View {
    var formId: Int = 0
    var accentHex: String? = nil
    var backgroundHex: String? = nil

    var body: some View {
        VStack(spacing: 0) {
            ZStack {
                portraitBackgroundColor

                PicoPortraitView(formId: formId, accentHex: accentHex)
            }
            .frame(maxWidth: .infinity, maxHeight: .infinity)

            Text("companion")
                .font(PicodFont.monoBoldSM)
                .kerning(PicodKerning.petLabel)
                .textCase(.uppercase)
                .foregroundColor(.picod_mid)
                .frame(maxWidth: .infinity)
                .frame(height: 18)
                .background(Color.picod_paper2)
                .overlay(alignment: .top) {
                    Rectangle().frame(height: 2).foregroundColor(.picod_ink)
                }
        }
        .frame(width: 104, height: 122)
        .background(Color.picod_paper)
        .clipShape(RoundedRectangle(cornerRadius: PicodBorder.petFrame))
        .overlay(
            RoundedRectangle(cornerRadius: PicodBorder.petFrame)
                .stroke(Color.picod_ink, lineWidth: PicodBorder.width)
        )
    }

    private var portraitBackgroundColor: Color {
        if let hex = backgroundHex, !hex.isEmpty {
            return Color(hex: hex)
        }
        return Color.picod_paper
    }
}

