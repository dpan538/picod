    var body: some View {
        GeometryReader { geo in
            let rows = normalizedRows24(pixelRows)
            let bounds = visibleBounds(for: rows)
            let activeCols = max(1, bounds.maxX - bounds.minX + 1)
            let activeRows = max(1, bounds.maxY - bounds.minY + 1)
            let edgeInset: CGFloat = 4
            let availableWidth = max(1, geo.size.width - edgeInset * 2)
            let availableHeight = max(1, geo.size.height - edgeInset * 2)
            let ps = max(1, floor(min(availableWidth / CGFloat(activeCols),
                                      availableHeight / CGFloat(activeRows))))
            let totalW = CGFloat(activeCols) * ps
            let totalH = CGFloat(activeRows) * ps
            let startX = floor((geo.size.width - totalW) / 2)
            let startY = floor((geo.size.height - totalH) / 2)

            ZStack(alignment: .topLeading) {
                Color.picod_paper2.opacity(0.65)

                ForEach(Array(rows.enumerated()), id: \.offset) { r, row in
                    ForEach(Array(row.enumerated()), id: \.offset) { c, char in
                        let fillColor = color(for: char, formId: formId)
                        if fillColor != .clear {
                            Rectangle()
                                .fill(fillColor)
                                .frame(width: ps, height: ps)
                                .position(
                                    x: startX + CGFloat(c - bounds.minX) * ps + ps / 2,
                                    y: startY + CGFloat(r - bounds.minY) * ps + ps / 2
                                )
                        }
                    }
                }
            }
        }
        .clipShape(RoundedRectangle(cornerRadius: 5))
    }

    private func normalizedRows24(_ rows: [String]) -> [String] {
        let targetWidth = 24
        let targetHeight = 24

        func normalizeRowWidth(_ row: String) -> String {
            let source = row.map { $0 == " " ? Character(".") : $0 }
            if source.count == targetWidth { return String(source) }

            if source.count > targetWidth {
                let extra = source.count - targetWidth
                let start = extra / 2
                return String(source[start..<(start + targetWidth)])
            }

            let missing = targetWidth - source.count
            let leftPad = missing / 2
            let rightPad = missing - leftPad
            return String(repeating: ".", count: leftPad) + String(source) + String(repeating: ".", count: rightPad)
        }

        var normalized = rows.map(normalizeRowWidth)

        if normalized.count > targetHeight {
            let extra = normalized.count - targetHeight
            let start = extra / 2
            normalized = Array(normalized[start..<(start + targetHeight)])
        } else if normalized.count < targetHeight {
            let missing = targetHeight - normalized.count
            let topPad = missing / 2
            let bottomPad = missing - topPad
            let blank = String(repeating: ".", count: targetWidth)
            normalized = Array(repeating: blank, count: topPad) + normalized + Array(repeating: blank, count: bottomPad)
        }

        return normalized
    }

