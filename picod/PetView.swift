    // Token legend:
    // . = transparent
    // 1 = light base, 2 = mid base, 3 = dark outline, 4 = detail A
    // 5 = accent, 6 = highlight, 7 = shadow mid, 8 = deepest dark
    // 9 = soft light, A = edge-softening color
    private func color(for token: Character, formId: Int) -> Color {
        let accent = accentHex.flatMap { Color(hex: $0) } ?? formTintColor
        if token == "5" {
            if formId == 2 { return Color(hex: "F8C8C8") }
            return accent
        }
        if let custom = Self.perFormColors(for: formId), let hex = custom[token] {
            return Color(hex: hex)
        }

        switch token {
        case "1": return Color(hex: "F0EDE8")
        case "2": return Color(hex: "D8D4CE")
        case "3": return Color(hex: "5A5550")
        case "4": return Color(hex: "2A2825")
        case "6": return Color(hex: "FFF7EA")
        case "7": return Color(hex: "383028")
        case "8": return Color(hex: "181008")
        case "9": return Color(hex: "F8F4F0")
        case "A": return Color(hex: "C0B8A8").opacity(0.5)
        default: return .clear
        }
    }

    private var formTintColor: Color {
        guard formId > 0 else { return Color(hex: "E8B0A0") }
        let hue = Double((formId * 37) % 360) / 360.0
        return Color(hue: hue, saturation: 0.52, brightness: 0.88)
    }
