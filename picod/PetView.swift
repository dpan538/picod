    private func visibleBounds(for rows: [String]) -> (minX: Int, maxX: Int, minY: Int, maxY: Int) {
        var minX = Int.max
        var maxX = Int.min
        var minY = Int.max
        var maxY = Int.min

        for (r, row) in rows.enumerated() {
            for (c, char) in row.enumerated() {
                // Bound only pixels that will actually draw. This avoids whitespace
                // placeholders expanding the bbox and visually shifting sprites.
                guard color(for: char, formId: formId) != .clear else { continue }
                minX = min(minX, c)
                maxX = max(maxX, c)
                minY = min(minY, r)
                maxY = max(maxY, r)
            }
        }

        if minX == Int.max || maxX == Int.min || minY == Int.max || maxY == Int.min {
            let width = max(1, rows.first?.count ?? 1)
            let height = max(1, rows.count)
            return (0, width - 1, 0, height - 1)
        }

        return (minX, maxX, minY, maxY)
    }

